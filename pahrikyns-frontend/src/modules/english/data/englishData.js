function buildLessons(topics) {
  const lessons = [];
  topics.forEach(t => {
    for (let i = 0; i < 10; i++) {
      const day = t.start + i;
      const v = t.vocab[i];
      lessons.push({
        day,
        title: `Day ${day}: ${t.topic}`,
        topic: t.topic,
        color: t.color,
        objective: `Understand and use ${t.topic.toLowerCase()} in everyday English.`,
        vocabulary: t.vocab.map(w => ({ word: w[0], meaning: w[1], example: w[2] })),
        focusWord: { word: v[0], meaning: v[1], example: v[2] },
        grammar: { rule: t.grammar, examples: t.grammarEx },
        practice: {
          instructions: `Complete these exercises based on today's topic: ${t.topic}.`,
          exercises: [
            `Write 3 sentences using the word "${v[0]}".`,
            `Create a sentence using today's grammar rule: ${t.grammar.split(":")[0]}.`,
            `Translate this to English and speak it aloud: "Today I learnt about ${t.topic}."`,
          ],
        },
        tip: t.tip,
      });
    }
  });
  return lessons;
}

